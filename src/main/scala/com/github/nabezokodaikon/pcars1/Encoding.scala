    val result: StringBuffer = new StringBuffer()
    var currentCodePage = codePageLatinToUnicode

    var i = 0
    while (i < byteArray.length) {
      if (byteArray(i) == '^') {
        if (i + 1 < byteArray.length) {
          i = i + 1
          byteArray(i) match {
            case 'E' =>
              currentCodePage = codePageCentralEuropeToUnicode
            case 'C' =>
              currentCodePage = codePageCyrillicToUnicode
            case 'L' =>
              currentCodePage = codePageLatinToUnicode
            case 'G' =>
              currentCodePage = codePageGreekToUnicode
            case 'T' =>
              currentCodePage = codePageTurkishToUnicode
            case 'B' =>
              currentCodePage = codePageBalticToUnicode
            case 'J' =>
              currentCodePage = codePageJapaneseToUnicode
            case 's' =>
              result.append('/')
            case 'd' =>
              result.append('\\')
            case 'a' =>
              result.append('*')
            case 'c' =>
              result.append(':')
            case 'q' =>
              result.append('?')
            case 't' =>
              result.append('"')
            case '<' =>
              result.append('<')
            case '>' =>
              result.append('>')
            case '^' =>
              result.append('^')
            case ':' =>
              result.append("^:")
              currentCodePage = codePageLatinToUnicode
            case _ =>
              i = i - 1
              result.append("^")
          }
        }
      } else {
        val index = (byteArray(i) & 0xFF);
        if (index > 127 && index <= 256) {
          result.append(Character.toString(currentCodePage(index)));
        } else {
          result.append(Character.toString(byteArray(i).toChar));
        }
      }
      i = i + 1
    }
    return result.toString().trim();
  }

